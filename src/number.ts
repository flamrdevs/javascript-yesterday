function number(min: number = 0, max: number = min + 1) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function average(array: number[]) {
  return array.reduce((prev, curr) => prev + curr, 0) / array.length;
}

//	====================================================================================
//	EXPORTS
//	====================================================================================

export { number, average };
